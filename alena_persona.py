def parse_diagnose_json(text: str) -> dict | None:
    """Устойчиво вытащить JSON-объект из ответа диагноза → dict | None.

    Терпит ```-обёртки и мусор вокруг: ищет первый сбалансированный {…} и
    json.loads. None при провале — вызывающий фолбэчит на безопасный дефолт."""
    if not text:
        return None
    s = text.strip()
    # Быстрый путь: чистый JSON.
    try:
        data = json.loads(s)
        return data if isinstance(data, dict) else None
    except Exception:
        pass
    # Ищем первый сбалансированный {…} с учётом строк/эскейпов.
    start = s.find("{")
    while start != -1:
        depth = 0
        in_str = False
        esc = False
        for i in range(start, len(s)):
            c = s[i]
            if in_str:
                if esc:
                    esc = False
                elif c == "\\":
                    esc = True
                elif c == '"':
                    in_str = False
                continue
            if c == '"':
                in_str = True
            elif c == "{":
                depth += 1
            elif c == "}":
                depth -= 1
                if depth == 0:
                    chunk = s[start:i + 1]
                    try:
                        data = json.loads(chunk)
                        if isinstance(data, dict):
                            return data
                    except Exception:
                        break  # этот кандидат битый — попробуем следующий '{'
        start = s.find("{", start + 1)
    return None
