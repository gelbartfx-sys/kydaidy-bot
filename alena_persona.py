def _client_model_brief(client_model: dict | None) -> str:
    """Компактный человекочитаемый срез текущей модели клиентки для входа диагноза."""
    if not client_model or not isinstance(client_model, dict):
        return "Модель клиентки пока пустая — строй её с нуля по разговору."
    order = ("pattern", "facade_lie", "true_request_hypothesis", "defenses",
             "objections", "readiness", "method_phase", "track", "given")
    lines = []
    for k in order:
        v = client_model.get(k)
        if v in (None, "", [], {}):
            continue
        if isinstance(v, (list, tuple)):
            v = ", ".join(str(x) for x in v if x)
            if not v:
                continue
        lines.append(f"- {k}: {v}")
    return "\n".join(lines) if lines else "Модель клиентки пока пустая — строй её с нуля."


