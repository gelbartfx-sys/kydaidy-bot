"""Персона «Алёны на связи»: голос, дуга встречи, кризис-guard.

build_system() собирает финальный systemInstruction для Gemini:
голос Алёны + KNOWLEDGE_CORE (экспертная опора) + дуга встречи + запреты тона
+ заметка о безопасности + контекст про конкретного человека.
"""

from __future__ import annotations

import json
import re

from ai_quiz import _FORBIDDEN
from alena_knowledge import KNOWLEDGE_CORE

# Маркер, которым модель помечает завершённую встречу (вырезаем перед отправкой).
CLOSE_MARK = "[[ВСТРЕЧА ЗАВЕРШЕНА]]"

# Маркер настоящего запроса — модель называет его в закрывающей реплике.
# Вырезается перед отправкой; текст идёт в нативный мост к 1:1.
REQUEST_RE = re.compile(r"\[\[\s*ЗАПРОС:\s*(.+?)\]\]", re.IGNORECASE | re.DOTALL)

# Маркер досье — модель отдаёт обновлённый портрет участницы (копится в БД).
# Вырезается перед отправкой; человек его не видит.
DOSSIER_RE = re.compile(r"\[\[\s*ДОСЬЕ:\s*(.+?)\]\]", re.IGNORECASE | re.DOTALL)

# Маркер скоринга (Фаза 1): модель скрыто оценивает собеседника по 4 сигналам
# (0–3). СЛУЖЕБНЫЙ — вырезается перед отправкой, человек его НИКОГДА не видит.
# Формат: [[SCORE ж=<0-3> о=<0-3> с=<0-3> ц=<0-3>]] (порядок и пробелы гибкие).
SCORE_RE = re.compile(
    r"\[\[\s*SCORE\b(.*?)\]\]", re.IGNORECASE | re.DOTALL)
_SCORE_FIELD_RE = re.compile(
    r"([жосцЖОСЦ])\s*=\s*([0-3])", re.IGNORECASE)
_SCORE_KEY = {"ж": "heat", "о": "open", "с": "resist", "ц": "value"}
# Защита от ОБРЕЗАННОГО маркера: если модель упёрлась в лимит токенов ровно на
# служебной строке ([[SCORE ж=2 … без закрывающих ]]), полный SCORE_RE не сработает.
# Этот срезает любой висячий «[[SCORE…» до конца строки, чтобы огрызок не утёк.
_SCORE_DANGLING_RE = re.compile(r"\[\[\s*SCORE\b.*$", re.IGNORECASE | re.DOTALL)

# Общая защита от ЛЮБОГО обрезанного маркера в хвосте: [[ЗАПРОС/[[ДОСЬЕ/[[ВСТРЕЧА…
# без закрывающих ]] (обрыв генерации по лимиту токенов). Раньше dangling-защита была
# только у SCORE → огрызок досье/запроса/close мог утечь человеку. Срезает трейлинг
# «[[…», который не закрылся «]]». Легит-реплика Алёны скобок [[ не содержит.
_DANGLING_MARKER_RE = re.compile(r"\[\[(?:(?!\]\]).)*$", re.DOTALL)


def strip_dangling_markers(text: str) -> str:
    """Убирает незакрытый служебный маркер в конце reply (полные уже извлекли extract_*)."""
    if not text:
        return text
    return _DANGLING_MARKER_RE.sub("", text).strip()


def extract_request(text: str) -> tuple[str, str | None]:
    """Вырезает маркер [[ЗАПРОС: ...]] из ответа. → (текст без маркера, запрос|None)."""
    if not text:
        return text, None
    m = REQUEST_RE.search(text)
    if not m:
        return text, None
    req = m.group(1).strip().strip("«»\"' .")
    clean = REQUEST_RE.sub("", text).strip()
    return clean, req


def extract_dossier(text: str) -> tuple[str, str | None]:
    """Вырезает маркер [[ДОСЬЕ: ...]] из ответа. → (текст без маркера, портрет|None)."""
    if not text:
        return text, None
    m = DOSSIER_RE.search(text)
    if not m:
        return text, None
    dossier = m.group(1).strip()
    clean = DOSSIER_RE.sub("", text).strip()
    return clean, dossier


def extract_score(text: str) -> tuple[str, dict | None]:
    """Вырезает служебный маркер [[SCORE ж=.. о=.. с=.. ц=..]] из ответа.

    → (текст БЕЗ маркера, {heat,open,resist,value} | None).
    Устойчив к отсутствию/частичному маркеру: если нашли хотя бы одно поле —
    вернём словарь с ним; ненайденные поля не добавляем (None-семантика на
    стороне БД). Маркер вырезается ВСЕГДА, как только распознан [[SCORE ...]] —
    чтобы служебка не утекла человеку.
    """
    if not text:
        return text, None
    m = SCORE_RE.search(text)
    # Чистим ВСЕГДА: и полный маркер, и висячий огрызок (обрезка ответа).
    clean = SCORE_RE.sub("", text)
    clean = _SCORE_DANGLING_RE.sub("", clean).strip()
    if not m:
        return clean, None
    signals: dict = {}
    for fm in _SCORE_FIELD_RE.finditer(m.group(1)):
        key = _SCORE_KEY.get(fm.group(1).lower())
        if key and key not in signals:
            signals[key] = int(fm.group(2))
    return clean, (signals or None)


